    private void TBNomEmpKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TBNomEmpKeyTyped
        int press = evt.getKeyChar();
        if(press == 10){
            String IDEncontrado = TBIDEmp.getText().trim();
            Empleados a;
            for (int i = 0 ; i < contenedor.size() ; i++){
                a = (Empleados)contenedor.get(i);
                if (IDEncontrado.equalsIgnoreCase(a.getID())){
                    TBNomEmp.setText(a.getNombre());
                    TBSalarEmp.setText(a.getSalario());
                    TBDepEmp.setText(a.getDepartamento());
                    buscar = i;
                    break;
                }
            }
        }
    }//GEN-LAST:event_TBNomEmpKeyTyped

