        String Salario = TBSalarEmp.getText();
        String Departamento = TBDepEmp.getText();
        
        Empleados claseEmpleados = new Empleados(nombre, ID, Salario, Departamento);
        contenedor.add(claseEmpleados);
        
        TBNomEmp.setText("");
